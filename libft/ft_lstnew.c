#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lstnew;

	lstnew = (t_list *) malloc(1 * sizeof(t_list));
	lstnew->content = content;
	lstnew->next = NULL;
	return (lstnew);
}
